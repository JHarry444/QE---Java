package methods.demo;

public class Greeter {

    public static void greeting() {
        System.out.println("Hello");
        System.out.println("World");
        System.out.println("!");
    }


    public static void greet(String name) {
        System.out.println("Hello");
        System.out.println(name);
        System.out.println("!");
    }
}
