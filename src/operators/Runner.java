package operators;

public class Runner {

    public static void main(String[] args) {
        System.out.println(2 + 2);
        System.out.println(2 - 2);
        System.out.println(2 * 2);
        System.out.println(2 / 2);
        System.out.println(9 % 2);


        System.out.println(Math.pow(3, 2));

        System.out.println(10 > 3);
        System.out.println(10 < 3);

        System.out.println(10 == (5 + 5));
        // ! -> NOT
        System.out.println(10 != (5 + 5));
        System.out.println(!true);
    }
}
